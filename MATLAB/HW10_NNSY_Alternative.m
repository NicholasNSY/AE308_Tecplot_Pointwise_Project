M1 = 3.0;

cl1file = dlmread('cl_case_1.txt');
cd1file = dlmread('cd_case_1.txt');

cl1data = cl1file(:,2);
cd1data = cd1file(:,2);

cl1 = mean(cl1data);
cd1 = mean(cd1data);

%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
%% Mach 1.0
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

M2 = 1.0;

cl2file = dlmread('cl_case_2.txt');
cd2file = dlmread('cd_case_2.txt');

cl2data = cl2file(:,2);
cd2data = cd2file(:,2);

cl2 = mean(cl2data);
cd2 = mean(cd2data);

%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
%% Mach 0.8
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

M3 = 0.8;

cl3file = dlmread('cl_case_3.txt');
cd3file = dlmread('cd_case_3.txt');

cl3data = cl3file(:,2);
cd3data = cd3file(:,2);

cl3 = mean(cl3data);
cd3 = mean(cd3data);

%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
%% Mach 0.3
%~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

M4 = 0.3;

